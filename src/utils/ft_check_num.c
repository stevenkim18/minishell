#include "minishell.h"

int				ft_check_num(const char *str)
{
	int			i;

	i = -1;
	while (*(str + ++i))
	{
		if (!('0' <= *(str + i) && *(str + i) <= '9'))
			return (-1);
	}
	return (0);
}
